class Solution {

    /**
     * å¤æ‚åº¦ O(n)
     * @param nums1
     * @param m
     * @param nums2
     * @param n
     */
    public void merge(int[] nums1, int m, int[] nums2, int n) {
        int nums1Index = m - 1;
        int nums2Index = n - 1;
        int index = m + n - 1;
        for (int i = 0;i < m + n;i++){
            if (nums1Index >= 0
                    && (nums2Index < 0 || nums1[nums1Index] >= nums2[nums2Index])){
                nums1[index--] = nums1[nums1Index--];
            }else {
                nums1[index--] = nums2[nums2Index--];
            }
        }
    }
}
